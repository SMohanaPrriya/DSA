package datastructures.hashtables;

public class KeyValuePair {
    private int key;
    private int value;


    //getter methods
    public int getKey() {
        return key;
    }

    public int getValue() {
        return value;
    }

    //setter method
    public void setKeyValuePair(int key, int value) {
        this.key = key;
        this.value = value;
    }

}
